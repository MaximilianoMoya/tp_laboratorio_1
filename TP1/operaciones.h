


/** \brief suma dos numeros y devuelve el resultado
 *
 * \param a float numero a sumar
 * \param b float numero a sumar
 * \return float resultado de la suma
 *
 */
float sumar( float a, float b);


/** \brief resta dos numeros y devuelve el resultado
 *
 * \param a float numero a restar
 * \param b float numero a restar
 * \return float resultado de la resta
 *
 */
float restar( float a, float b);

/** \brief divide entre dos numeros y devuelve el resultado
 *
 * \param a float numero a dividir
 * \param b float numero divisor
 * \return float resultado de la division
 *
 */
float dividir( float a, float b);

/** \brief multiplica entre dos numeros y devuelve el resultado
 *
 * \param a float numero a multiplicar
 * \param b float numero por el que se multiplica
 * \return float resultado de la multiplicacion
 *
 */
float multiplicar( float a, float b);

/** \brief Calcula el factorial de un numero
 *
 * \param num float numero del cual se obtendra el factorial
 * \return double resultado, -1 si el numero es decimal menor a 0 o -2 si el numero es demasiado grande para el tamanio de la memoria (si float num es mayor a 170)
 *
 */
double factorial(float num);
